				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='jianzhumingcheng'){
							this.ruleForm.jianzhumingcheng = obj[o];
							this.ro.jianzhumingcheng = true;
							continue;
						}
						if(o=='zhaopian'){
							this.ruleForm.zhaopian = obj[o];
							this.ro.zhaopian = true;
